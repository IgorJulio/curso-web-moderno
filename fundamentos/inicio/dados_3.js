 /*
  Esses são os três tipos básicos suportado pelo JavaScript:
  Numero, verdadeiro ou falso e string que é uma sequência de caracteres.  
  Typeof é uma função para saber o tipo de informação 
  */

let idade = 31;
console.log(typeof 31);
console.log(typeof idade);

let salario = 4578.32;
console.log(typeof salario);
console.log(typeof 4578.32);

let estaChovendo = true // ou false 
console.log(typeof true);
console.log(typeof estaChovendo);

console.log(typeof "teste");
console.log(typeof 'teste');







